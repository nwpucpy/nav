    q1 = Xkk_1(2);
    q2 = Xkk_1(3);
    q3 = Xkk_1(4);


    H_YAW = zeros(1,16);
    H_YAW(1,1) = (((2*q3)/(q0^2 + q1^2 - q2^2 - q3^2) - (2*q0*(2*q0*q3 + 2*q1*q2))/(q0^2 + q1^2 - q2^2 - q3^2)^2)*(q0^2 + q1^2 - q2^2 - q3^2)^2)/((2*q0*q3 + 2*q1*q2)^2 + (q0^2 + q1^2 - q2^2 - q3^2)^2);
    H_YAW(1,2) = (((2*q2)/(q0^2 + q1^2 - q2^2 - q3^2) - (2*q1*(2*q0*q3 + 2*q1*q2))/(q0^2 + q1^2 - q2^2 - q3^2)^2)*(q0^2 + q1^2 - q2^2 - q3^2)^2)/((2*q0*q3 + 2*q1*q2)^2 + (q0^2 + q1^2 - q2^2 - q3^2)^2);
    H_YAW(1,3) = (((2*q1)/(q0^2 + q1^2 - q2^2 - q3^2) + (2*q2*(2*q0*q3 + 2*q1*q2))/(q0^2 + q1^2 - q2^2 - q3^2)^2)*(q0^2 + q1^2 - q2^2 - q3^2)^2)/((2*q0*q3 + 2*q1*q2)^2 + (q0^2 + q1^2 - q2^2 - q3^2)^2);
    H_YAW(1,4) = (((2*q0)/(q0^2 + q1^2 - q2^2 - q3^2) + (2*q3*(2*q0*q3 + 2*q1*q2))/(q0^2 + q1^2 - q2^2 - q3^2)^2)*(q0^2 + q1^2 - q2^2 - q3^2)^2)/((2*q0*q3 + 2*q1*q2)^2 + (q0^2 + q1^2 - q2^2 - q3^2)^2);


end